def specRealization(G,phi=None):
    # G is a networkx graph 
    # phi = squared edge-length constraint
    
    # edge-veretx incidence matrix
    B = nx.incidence_matrix(G,oriented=True).todense().transpose()

    m = B.shape[0]
    n = B.shape[1]
    print(f"{G.name} graph with {m} edges and {n} vertices")

    if phi is None:
        phi=np.ones(m)
    
    w = cp.Variable(m)
    t = cp.Variable(1)
    L = cp.Variable([n,n])
    
    J = np.eye(n) - (1/n)*np.outer(np.ones(n),np.ones(n))
    constraints = [w>=0, phi@w==1, L == B.T@cp.diag(w)@B, L >> t*J]
    prob = cp.Problem(cp.Maximize(t),constraints)
    prob.solve(solver=cp.SCS,verbose=False,eps=1e-10,max_iters=10_000)

    Y = constraints[3].dual_value
    L = L.value
    w = w.value

    print(prob.status)

    print(f"Optimal value = {prob.value}")

    lam, U = np.linalg.eigh(L)
    print(f"lam = {lam}")

    mult = np.sum(np.abs(lam[1:] - lam[1]) < 1e-6)
    print(f"multiplicity = {mult}")

    if np.any(w<=1e-5): print("Warning, zero w values!!!")
    print("w = ")
    print(w)

    beta,EVY = np.linalg.eigh(Y)
    print(f"beta = {beta}")

    X = EVY[:,-mult:]@np.diag(np.sqrt(beta[-mult:]))/np.sqrt(constraints[1].dual_value)
    edge_lens = np.sqrt(np.sum(np.power(B@X,2),1)).flatten()
    
    print("edge lengths = ")
    print(edge_lens)
    print(f"variance in edge_lens is {np.var(edge_lens)}")

    totVar = np.linalg.norm(X,'fro')**2
    print(f'||X||_F^2 = {totVar}')    

    spec_real = {'X':X,
                 'mult':mult,
                 'lam':lam,
                 'w':w}
    return spec_real 


def plotRealization3d(G, spec_real,save_tf=True):
    pos = spec_real['X']
    normalization = np.max(np.linalg.norm(spec_real['X'][:,:3],axis=1))
    pos = pos/normalization
    w = spec_real['w']

    node_xyz = np.array([pos[v] for v,_ in enumerate(sorted(G))])
    edge_xyz = np.array([(pos[u], pos[v]) for u, v in G.edges()])

    # Create the 3D figure
    fig = plt.figure(figsize=(8, 6),dpi=300)
    ax = fig.add_subplot(111, projection="3d")

    # Plot the nodes - alpha is scaled by "depth" automatically
    ax.scatter(*node_xyz.T, s=100)

    cmap = cm.get_cmap('viridis') 
    cmap.set_under('black')

    # Plot the edges
    w_max = np.max(w)
    w_min = np.min(w)
    cs = (w - 1e-5) / w_max
    for ii,vizedge in enumerate(edge_xyz):
        ax.plot(*vizedge.T, color=cmap(cs[ii]))


    sm = plt.cm.ScalarMappable(cmap=cmap, norm=plt.Normalize(vmin=0, vmax=1))
    sm._A = []
    plt.colorbar(sm, shrink=0.53, pad=0.01,ticks=[],orientation='horizontal',extend='both')

    # draw sphere
    u, v = np.mgrid[0:2*np.pi:50j, 0:np.pi:50j]
    x = np.cos(u)*np.sin(v)
    y = np.sin(u)*np.sin(v)
    z = np.cos(v)
    ax.plot_surface(x, y, z, color="tab:blue", alpha=0.1)

    ax.grid(True)
    for dim in (ax.xaxis, ax.yaxis, ax.zaxis): dim.set_ticks(np.arange(-1, 1.1, step=0.5))

    fig.tight_layout()

    ax.set_aspect('equal', adjustable='box')
    #plt.axis('equal')
    
    if save_tf:
        plt.savefig('../figs/'+G.name,bbox_inches = 'tight',pad_inches = 0.1)
    return


